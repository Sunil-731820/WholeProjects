let x = 100; // 100 will be assign to the x 
console.log("the value of the x is "+ x);

// += assignment add operator

x +=100;
console.log("the value of the x after increment value by 100 is " + x);

// the -= assignment operator subtracts a value from a variable.

x -=5;
console.log("after decreasing the value of the 5 from x is "+ x);

// The *= assignment operator multiplies a variable

x *=10;
console.log("after multiplying the remaining value by 10 is "+ x);

// The /= assignment divides a variable.

x /=5;
console.log("after dividing the x by 5 is "+ x);
